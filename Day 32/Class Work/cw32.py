# სიის (list) ყველაზე ხშირად გამოყენებადი მეთოდები
# ქვემოთ თითოეულს ახსნილია რას აკეთებს + 2 მაგალითი

fruits = ["ვაშლი", "ბანანი", "ალუბალი"]

# .append(x) → ამატებს ელემენტს სიის ბოლოში
fruits.append("მანდარინი")
print(fruits)  

numbers = [1, 2, 3]
numbers.append(100)
print(numbers)  


# .clear() → შლის სიიდან ყველა ელემენტს (სია ხდება ცარიელი)
temp = [10, 20, 30, 40]
temp.clear()
print(temp)  # []


# .copy() → ქმნის სიის ზედაპირულ ასლს (shallow copy)
original = [1, 2, [3, 4]]
copy_list = original.copy()

copy_list[0] = 999
print(original)   
print(copy_list)  


# .count(x) → ითვლის რამდენჯერ გვხვდება ელემენტი x სიაში
letters = ["ა", "ბ", "ა", "გ", "ა", "დ"]
print(letters.count("ა"))    
print(letters.count("ზ"))     


# .extend(iterable) → ამატებს სხვა იტერირებადი ობიექტის ყველა ელემენტს (არა მთლიანად!)
veggies = ["კარტოფილი", "სტაფილო"]
more = ["ბროკოლი", "კომბოსტო"]

veggies.extend(more)
print(veggies)  # ['კარტოფილი', 'სტაფილო', 'ბროკოლი', 'კომბოსტო']

# შედარებისთვის: append-ის შემთხვევაში დაემატებოდა მთლიანი სია
veggies.append(["კიტრი", "პომიდორი"])
print(veggies)  # ['კარტოფილი', ..., 'კომბოსტო', ['კიტრი', 'პომიდორი']]


# .index(x) → აბრუნებს პირველი x-ის ინდექსს (ValueError თუ არ არსებობს)
colors = ["წითელი", "მწვანე", "ლურჯი", "ყვითელი", "მწვანე"]
print(colors.index("მწვანე"))    # 1 (პირველი გამოჩენა)
# print(colors.index("შავი"))     # → ValueError: 'შავი' is not in list


# .insert(index, x) → ამატებს x-ს მითითებულ ინდექსზე (დანარჩენი ელემენტები გადაიწევს)
animals = ["ძაღლი", "კატა", "თევზი"]
animals.insert(1, "ფრინველი")
print(animals)  # ['ძაღლი', 'ფრინველი', 'კატა', 'თევზი']

animals.insert(0, "გველი")     # დასაწყისში ჩასმა
print(animals)  # ['გველი', 'ძაღლი', 'ფრინველი', 'კატა', 'თევზი']


# .pop([index]) → შლის და აბრუნებს ელემენტს მითითებულ ინდექსზე
# თუ ინდექსი არაა მითითებული → ბოლო ელემენტი
nums = [10, 20, 30, 40, 50]

last = nums.pop()       
print(last, nums)        

second = nums.pop(1)     
print(second, nums)      


# .remove(x) → შლის პირველად გამოჩენილ x-ს (ValueError თუ არ არსებობს)
names = ["ნიკა", "გიორგი", "ანა", "გიორგი"]
names.remove("გიორგი")          
print(names)                   


# .reverse() → აბრუნებს სიას შებრუნებულ თანმიმდევრობით (ცვლის ორიგინალს)
numbers = [1, 2, 3, 4, 5]
numbers.reverse()
print(numbers) 


# .sort() → ალაგებს სიას ზრდადობით (ცვლის ორიგინალს)
# შეიძლება გამოვიყენოთ key= და reverse=True პარამეტრებით

scores = [45, 12, 89, 3, 67]
scores.sort()
print(scores)          

names = ["ზურა", "ანა", "გიორგი", "ნიკა"]
names.sort(reverse=True)
print(names)            