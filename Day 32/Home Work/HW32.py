# рЃЏрЃљрЃњрЃарЃљрЃЏ рЃњрЃљрЃЏрЃЮрЃФрЃљрЃ«рЃћрЃЉрЃўрЃАрЃљрЃА рЃњрЃљрЃЊрЃљрЃћрЃфрЃљ рЃЮрЃарЃў рЃљрЃарЃњрЃБрЃЏрЃћрЃюрЃбрЃў ("nika" рЃЊрЃљ "saba")
# Python-рЃА рЃљрЃа рЃерЃћрЃБрЃФрЃџрЃўрЃљ рЃЏрЃўрЃљрЃюрЃўрЃГрЃЮрЃА рЃЮрЃарЃў рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃљ рЃћрЃарЃЌ рЃърЃљрЃарЃљрЃЏрЃћрЃбрЃарЃА
