# 1) რა არის სია (list) და რამდენი მონაცემთა ტიპის შენახვა შეგვიძლია
"""
სია (list) — ეს არის მონაცემთა სტრუქტურა Python-ში, რომელიც ინახავს ელემენტების თანმიმდევრობას.
- იწერება კვადრატულ ფრჩხილებში: [elem1, elem2, ...]
- ელემენტები ინდექსირდება 0-დან (0, 1, 2, ...)
- შეიძლება შეიცვალოს (mutable)
- შეიძლება იყოს ცარიელი: []

რამდენი ტიპის მონაცემი შეგვიძლია შევინახოთ?
→ ნებისმიერი ტიპის! ერთ სიაში შეიძლება იყოს:
int, float, str, bool, list, dict, tuple, set, None და ა.შ. — ყველა ერთად!
მაგ: [1, "hello", 3.14, True, [5,6], None]
"""
print("დავალება 1: ახსნა ზემოთ კომენტარებში")
print("-" * 60)


# 2) ოჯახის წევრების სახელები + 1-ლი და 3-ე ელემენტი
print("დავალება 2:")
names = ["ედიტა", "გიორგი", "ნატა", "ნიკოლოზ"]  # დედა, მამა, და, მე

print("პირველი ელემენტი (ინდექსი 0):", names[0])
print("მესამე ელემენტი (ინდექსი 2):", names[2])
print("-" * 60)


# 3) numbers სია 10 რენდომ რიცხვით + 0-ლი ინდექსის შეცვლა სახელით
print("დავალება 3:")
import random

numbers = [random.randint(1, 100) for _ in range(10)]  # 10 შემთხვევითი რიცხვი
print("თავდაპირველი სია:", numbers)

numbers[0] = "ნიკოლოზ"  # 0-ლი ინდექსის შეცვლა
print("ახალი სია (0-ლი ინდექსი = სახელი):", numbers)
print("-" * 60)


# 4) სია სხვადასხვა ტიპის მონაცემებით + კენტ ინდექსებზე მდგომი ელემენტები
print("დავალება 4:")
mixed_list = [
    17,              # int - ასაკი
    "ნიკოლოზ",      # str - სახელი
    174.5,           # float - სიმაღლე
    True,            # bool
    ["პანდა", "ლომი"],  # list
    None,            # NoneType
    (1, 2, 3),       # tuple
    {"ქალაქი": "თბილისი"},  # dict
    61,              # int - წონა
    False            # bool
]

print("სრული სია:", mixed_list)
print("კენტ ინდექსებზე მდგომი ელემენტები (1, 3, 5, 7, 9):")
for i in range(1, len(mixed_list), 2):  # იწყება 1-დან, ნაბიჯი +2
    print(f"ინდექსი {i}: {mixed_list[i]}")


    