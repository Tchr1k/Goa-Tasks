# 1. რა არის მეთოდი და რა არის მისი სინტაქსი?

# მეთოდი — ეს არის ფუნქცია, რომელიც "მიბმულია" კონკრეტულ ობიექტზე (მაგ. სტრიქონზე, სიაზე, რიცხვზე და ა.შ.)
# განსხვავება ჩვეულებრივი ფუნქციისგან: მეთოდი იწერება წერტილის (.) შემდეგ და მუშაობს იმ ობიექტზე, რომელზეც გამოიძახება

# სინტაქსი:
# ობიექტი.მეთოდის_სახელი(არგუმენტები)

# მაგალითად:
"hello".upper()        # აქ upper() არის მეთოდი, რომელიც მუშაობს სტრიქონ "hello"-ზე
[1, 2, 3].append(4)    # append() არის მეთოდი სიისთვის


# 2. დღევანდელ გაკვეთილზე ნასწავლი სტრიქონის მეთოდები + მაგალითები

text = "გამარჯობა, Python-ის მოსწავლეები! 2026"

# upper() — ყველა ასოს ადიდებს
print(text.upper())                  
# გამარჯობა, PYTHON-ის მოსწავლეები! 2026

# lower() — ყველა ასოს აპატარავებს
print("HELLO WORLD".lower())         
# hello world

# capitalize() — პირველ ასოს ადიდებს, დანარჩენს აპატარავებს
print("გამარჯობა nika".capitalize())  
# გამარჯობა nika

# find() — აბრუნებს პირველი შესაბამისი სიმბოლოს/სიტყვის ინდექსს (-1 თუ ვერ იპოვა)
print("hello world".find("world"))   
# 6
print("hello world".find("python"))  
# -1

# count() — ითვლის რამდენჯერ გვხვდება სიმბოლო/სიტყვა
print("banana".count("a"))           
# 3
print("hello hello hi".count("hello")) 
# 2

# isupper() — ამოწმებს, არის თუ არა ყველა ასო დიდი (True/False)
print("HELLO".isupper())             
# True
print("Hello".isupper())             
# False

# islower() — ამოწმებს, არის თუ არა ყველა ასო პატარა
print("hello".islower())             
# True
print("Hello".islower())             
# False

# startswith() — ამოწმებს იწყება თუ არა სტრიქონი მითითებული სიმბოლოებით
print("https://goa.ge".startswith("https"))  
# True
print("photo.jpg".startswith("img"))         
# False

# endswith() — ამოწმებს მთავრდება თუ არა სტრიქონი მითითებული სიმბოლოებით
print("document.pdf".endswith(".pdf"))       
# True
print("image.png".endswith(".jpg"))          
# False

# isalnum() — ამოწმებს შედგება თუ არა სტრიქონი მხოლოდ ასოებისა და ციფრებისგან (სფეისები და სიმბოლოები არ შეიძლება)
print("python2026".isalnum())        
# True
print("python 2026".isalnum())       
# False (სფეისის გამო)
print("hello!".isalnum())            
# False (გამონათქვამის ნიშნის გამო)

# isalpha() — ამოწმებს შედგება თუ არა სტრიქონი მხოლოდ ასოებისგან
print("გამარჯობა".isalpha())          
# True
print("hello123".isalpha())          
# False

# isdigit() — ამოწმებს შედგება თუ არა სტრიქონი მხოლოდ ციფრებისგან
print("12345".isdigit())             
# True
print("123abc".isdigit())            
# False
print("½".isdigit())                 
# False (ფრაქცია არ ითვლება)

# swapcase() — დიდ ასოებს აპატარავებს, პატარებს ადიდებს
print("HeLlO WoRlD".swapcase())      
# hElLo wOrLd
print("გამარჯობა PyThOn".swapcase())   
# გამარჯობა pYtHoN