# 5) ოჯახის წევრები + მეორე ინდექსის (3-ე ელემენტი) შეცვლა

ოჯახი = ["დედა", "მამა", "და", "ძმა", "ბებია"]

# ინდექსები:   0      1      2     3      4
ოჯახი[2] = "ლომი"      # მეორე ინდექსი = 3-ე ადგილი
