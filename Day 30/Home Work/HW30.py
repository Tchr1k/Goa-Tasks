# 2) len() ფუნქცია

# len() გვაბრუნებს ობიექტის სიგრძეს (ელემენტების რაოდენობას)
# მუშაობს: სიებზე, სტრიქონებზე, ტუპლებზე, კრებულებზე და ა.შ.

