# 3) სიის ყველაზე ხშირად გამოყენებადი მეთოდები დღევანდელ გაკვეთილზე

# .append(ელემენტი) → ამატებს ელემენტს სიის ბოლოში
ფერები = ["წითელი", "ლურჯი"]
ფერები.append("მწვანე")
print(ფერები)          # ['წითელი', 'ლურჯი', 'მწვანე']

# .insert(ინდექსი, ელემენტი) → ამატებს ელემენტს მითითებულ პოზიციაზე
ცხოველები = ["ძაღლი", "კატა", "თევზი"]
ცხოველები.insert(1, "ფრინველი")
print(ცხოველები)      # ['ძაღლი', 'ფრინველი', 'კატა', 'თევზი']

# .pop(ინდექსი) → შლის და აბრუნებს ელემენტს მითითებულ ინდექსზე
# თუ ინდექსი არ მიეცა — შლის და აბრუნებს ბოლო ელემენტს
რიცხვები = [10, 20, 30, 40, 50]
წაშლილი = რიცხვები.pop(2)     # შლის 30-ს (ინდექსი 2)
print(წაშლილი)               # 30
print(რიცხვები)               # [10, 20, 40, 50]

ბოლო = რიცხვები.pop()         # შლის ბოლოს (50)
print(ბოლო)                   # 50

# ---------------------------------------------------

# 4) სია ნებისმიერი ტიპის მნიშვნელობებით + ბოლოში სახელის დამატება

შერეული_სია = [42, "გამარჯობა", 3.14, True, None, [1, 2, 3]]

შერეული_სია.append("ნიკოლოზ")   # ან შენი სახელი
