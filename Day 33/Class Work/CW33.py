# 1) and და or ლოგიკური ოპერატორები (კომენტარებით)

# and → ჭეშმარიტია მხოლოდ მაშინ, როცა ორივე პირობა ჭეშმარიტია
# or  → ჭეშმარიტია თუ მაინც ერთი პირობა მაინც ჭეშმარიტია

# მაგალითები and-ზე:
age = 25
has_license = True

print(age >= 18 and has_license)   
print(age >= 18 and not has_license)   

# მაგალითები or-ზე:
is_weekend = False
is_holiday = True

print(is_weekend or is_holiday)       
print(is_weekend or False)              


# 2) სახელის გადატარება for ციკლით (ასოების ცალ-ცალკე გამოტანა)

my_name = "ნიკოლოზ"

for ასო in my_name:
    print(ასო)



# 3) while ციკლი — 1-დან 20-მდე ლუწი რიცხვები

რიცხვი = 1

while რიცხვი <= 20:
    if რიცხვი % 2 == 0:
        print(რიცხვი)
    რიცხვი += 1

# 4) შერეული სია + მეორე ინდექსის წაშლა + ბოლოში სახელის დამატება

შერეული = [42, "გამარჯობა", 3.14, True, [1,2,3], None, "python", 2026, False, "test"]

# მეორე ინდექსზე მდგომი ელემენტი (ინდექსი 2 → 3.14)
შერეული.pop(2)

# ბოლოში ჩემი სახელი
შერეული.append("ნიკოლოზ")

print(შერეული)
