# РЂАРЂбРЂбРЂБ­ЮЪГ) рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃерЃћрЃЏрЃЮрЃљрЃбрЃљрЃюрЃўрЃюрЃўрЃюрЃћ ­ЮЪГ рЃЊрЃљрЃю ­ЮЪ«­ЮЪг рЃљрЃЏрЃЊрЃћ рЃюрЃћрЃЉрЃўрЃАрЃЏрЃўрЃћрЃарЃў рЃарЃўрЃфрЃ«рЃЋрЃў. рЃЌрЃБ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃерЃћрЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃарЃўрЃфрЃ«рЃЋрЃў рЃюрЃљрЃЎрЃџрЃћрЃЉрЃўрЃљ ­ЮЪГ­ЮЪг рЃќрЃћ рЃљрЃю рЃЏрЃћрЃбрЃўрЃљ рЃЮрЃфрЃќрЃћ рЃЏрЃљрЃерЃўрЃю рЃЊрЃљрЃърЃарЃўрЃюрЃбрЃћ "­ЮЌ┤­ЮЌ╝­ЮЌ╝­ЮЌ▒". рЃ«рЃЮрЃџрЃЮ рЃАрЃ«рЃЋрЃљ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў рЃЊрЃљрЃърЃарЃўрЃюрЃбрЃћ "­ЮЌ»­ЮЌ«­ЮЌ▒".

# ­ЮЪ«) рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃўрЃЌ рЃљрЃ«рЃАрЃћрЃюрЃўрЃЌ рЃарЃљрЃА рЃљрЃЎрЃћрЃЌрЃћрЃЉрЃА ­ЮЌХ­ЮЌ│, ­ЮЌ▓­ЮЌ╣­ЮЌХ­ЮЌ│, ­ЮЌ▓­ЮЌ╣­Юўђ­ЮЌ▓.

# ­ЮЪ») рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃерЃћрЃЏрЃЮрЃљрЃбрЃљрЃюрЃўрЃюрЃћ рЃерЃћрЃюрЃў рЃљрЃАрЃљрЃЎрЃў. рЃЌрЃБ рЃерЃћрЃюрЃў рЃљрЃАрЃљрЃЎрЃў рЃЏрЃћрЃбрЃўрЃљ рЃљрЃю рЃбрЃЮрЃџрЃў ­ЮЪГ­ЮЪ┤ рЃќрЃћ рЃЏрЃљрЃерЃўрЃю рЃЊрЃљрЃърЃарЃўрЃюрЃбрЃћ "рЃАрЃарЃБрЃџрЃгрЃџрЃЮрЃЋрЃљрЃюрЃў". рЃ«рЃЮрЃџрЃЮ рЃАрЃ«рЃЋрЃљ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў рЃЊрЃљрЃърЃарЃўрЃюрЃбрЃћ рЃљрЃарЃљрЃАрЃарЃБрЃџрЃгрЃџрЃЮрЃЋрЃљрЃюрЃў.

# ­ЮЪ░) рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃерЃћрЃЏрЃЮрЃљрЃбрЃљрЃюрЃўрЃюрЃћ рЃюрЃћрЃЉрЃўрЃАрЃЏрЃўрЃћрЃарЃў рЃарЃўрЃфрЃ«рЃЋрЃў. рЃЌрЃБ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃерЃћрЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃарЃўрЃфрЃ«рЃЋрЃў рЃўрЃДрЃЮрЃцрЃљ ­ЮЪ«-рЃќрЃћ рЃЊрЃљ рЃюрЃљрЃерЃЌрЃў рЃарЃЕрЃћрЃЉрЃљ ­ЮЪг-рЃў рЃЊрЃљрЃърЃарЃўрЃюрЃбрЃћ ­ЮЌў­ЮўЃ­ЮЌ▓­ЮЌ╗. рЃ«рЃЮрЃџрЃЮ рЃАрЃ«рЃЋрЃљ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў рЃЊрЃљрЃърЃарЃўрЃюрЃбрЃћ ­ЮЌб­ЮЌ▒­ЮЌ▒.

# ­ЮЪ▒) рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃерЃћрЃЏрЃЮрЃљрЃбрЃљрЃюрЃўрЃюрЃћ рЃЦрЃБрЃџрЃљ ­ЮЪГ рЃЊрЃљрЃю ­ЮЪГ­ЮЪг-рЃЏрЃЊрЃћ рЃЌрЃБ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃЦрЃБрЃџрЃљ рЃЏрЃћрЃбрЃўрЃљ рЃљрЃю рЃбрЃЮрЃџрЃў ­ЮЪ┤-рЃќрЃћ рЃЏрЃљрЃњ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў рЃЊрЃљрЃърЃарЃўрЃюрЃбрЃћ "рЃЌрЃЦрЃЋрЃћрЃю рЃгрЃљрЃарЃЏрЃљрЃбрЃћрЃЉрЃўрЃЌ рЃЕрЃљрЃљрЃЉрЃљрЃарЃћрЃЌ рЃњрЃљрЃЏрЃЮрЃфрЃЊрЃљ". рЃ«рЃЮрЃџрЃЮ рЃАрЃ«рЃЋрЃљ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў "рЃЌрЃЦрЃЋрЃћрЃю рЃЕрЃљрЃўрЃГрЃћрЃарЃўрЃЌ.РЂА

