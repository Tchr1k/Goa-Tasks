# 1) рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў 10-рЃЊрЃљрЃю 15-рЃЏрЃЊрЃћ (рЃЕрЃљрЃЌрЃЋрЃџрЃўрЃЌ)
print("While 1) 10-рЃЊрЃљрЃю 15-рЃЏрЃЊрЃћ:")
i = 10
while i <= 15:
    print(i, end=" ")
    i += 1
print("\n")


# 2) рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў 50-рЃЊрЃљрЃю 0-рЃЏрЃЊрЃћ (рЃЕрЃљрЃЌрЃЋрЃџрЃўрЃЌ)
print("While 2) 50-рЃЊрЃљрЃю 0-рЃЏрЃЊрЃћ:")
i = 50
while i >= 0:
    print(i, end=" ")
    i -= 1
print("\n")


# 3) рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃџрЃБрЃгрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў 10-рЃЊрЃљрЃю 70-рЃЏрЃЊрЃћ
print("While 3) рЃџрЃБрЃгрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў 10-рЃЊрЃљрЃю 70-рЃЏрЃЊрЃћ:")
i = 10
while i <= 70:
    if i % 2 == 0:
        print(i, end=" ")
    i += 1
print("\n")


# 4) рЃЎрЃћрЃюрЃбрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў 25-рЃЊрЃљрЃю 55-рЃЏрЃЊрЃћ
print("While 4) рЃЎрЃћрЃюрЃбрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў 25-рЃЊрЃљрЃю 55-рЃЏрЃЊрЃћ:")
