# РЂАРЂбРЂбРЂБ­ЮЪГ) рЃерЃћрЃЦрЃЏрЃћрЃюрЃў рЃфрЃЋрЃџрЃљрЃЊрЃў рЃАрЃљрЃЊрЃљрЃф рЃерЃћрЃўрЃюрЃљрЃ«рЃљрЃЋ рЃерЃћрЃюрЃА рЃАрЃљрЃДрЃЋрЃљрЃарЃћрЃџ рЃАрЃърЃЮрЃарЃбрЃўрЃА рЃАрЃљрЃ«рЃћрЃџрЃА рЃерЃћрЃЏрЃЊрЃћрЃњ ­ЮЎЏ­ЮЎц­ЮЎД рЃфрЃўрЃЎрЃџрЃўрЃЌ рЃњрЃљрЃЊрЃљрЃБрЃљрЃарЃћрЃЌ рЃфрЃЋрЃџрЃљрЃЊрЃА рЃЊрЃљ рЃњрЃљрЃЏрЃЮрЃўрЃбрЃљрЃюрЃћрЃЌ рЃЌрЃЦрЃЋрЃћрЃюрЃў рЃАрЃљрЃДрЃЋрЃљрЃарЃћрЃџрЃў рЃАрЃърЃЮрЃарЃбрЃўрЃА рЃАрЃљрЃ«рЃћрЃџрЃў рЃфрЃљрЃџ-рЃфрЃљрЃџрЃЎрЃћ.

# ­ЮЪ«) рЃерЃћрЃЦрЃЏрЃћрЃюрЃў ­ЮЎг­ЮЎЮ­ЮЎъ­ЮЎА­ЮЎџ рЃфрЃўрЃЎрЃџрЃў. рЃАрЃљрЃЊрЃљрЃф рЃњрЃљрЃЏрЃЮрЃўрЃбрЃљрЃю рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃА ­ЮЪГ рЃЊрЃљрЃю ­ЮЪГ­ЮЪг­ЮЪг рЃЏрЃЊрЃћ рЃЊрЃљ рЃДрЃЮрЃЋрЃћрЃџ рЃўрЃбрЃћрЃарЃљрЃфрЃўрЃљрЃќрЃћ рЃБрЃюрЃЊрЃљ рЃЊрЃљрЃБрЃЏрЃљрЃбрЃЮ ­ЮЪ░-рЃўрЃљрЃюрЃў.

# ­ЮЪ») рЃерЃћрЃЦрЃЏрЃћрЃюрЃў ­ЮЎЏ­ЮЎц­ЮЎД рЃфрЃўрЃЎрЃџрЃў рЃАрЃљрЃЊрЃљрЃф рЃњрЃљрЃЏрЃЮрЃўрЃбрЃљрЃю ­ЮЪг-рЃЊрЃљрЃю  ­ЮЪ░­ЮЪх­ЮЪх рЃўрЃА рЃЕрЃљрЃЌрЃЋрЃџрЃўрЃЌ рЃџрЃБрЃг рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃА.

# ­ЮЪ░) рЃЊрЃљрЃгрЃћрЃарЃћ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃўрЃЌрЃ«рЃЮрЃЋрЃА рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃАрЃљрЃДрЃЋрЃљрЃарЃћрЃџ рЃарЃўрЃфрЃ«рЃЋрЃА рЃЊрЃљ рЃљрЃа рЃЕрЃћрЃарЃЊрЃћрЃЉрЃљ рЃЏрЃљрЃюрЃљрЃЏ, рЃАрЃљрЃюрЃљрЃЏ рЃАрЃгрЃЮрЃа рЃарЃўрЃфрЃ«рЃЋрЃА рЃљрЃа рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃА.рЃЌрЃБ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃА рЃАрЃ«рЃЋрЃљ рЃфрЃўрЃцрЃарЃА, рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљрЃЏ рЃБрЃюрЃЊрЃљ рЃАрЃЌрЃ«рЃЮрЃЋрЃЮрЃА рЃЌрЃљрЃЋрЃўрЃЊрЃљрЃю рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃЮрЃА.рЃарЃЮрЃЊрЃћрЃАрЃљрЃф рЃАрЃгрЃЮрЃарЃў рЃарЃўрЃфрЃ«рЃЋрЃў рЃўрЃЦрЃюрЃћрЃЉрЃљ рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў Рђћ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљрЃЏ рЃБрЃюрЃЊрЃљ рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃЮрЃА, рЃарЃЮрЃЏ рЃћрЃА рЃърЃљрЃАрЃБрЃ«рЃў рЃАрЃгрЃЮрЃарЃўрЃљ.РЂА

