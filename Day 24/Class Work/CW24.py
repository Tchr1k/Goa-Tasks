# 1) რა არის Slicing და რაში გამოიყენება?

# Slicing არის სტრიქონის (ან სიის, ტუპლის) გარკვეული ნაწილის ამოღება/გამოყოფა
# ძირითადი ფორმა:  variable[start:stop:step]
# start → საიდან იწყება (ჩათვლით)
# stop  → სადამდე გრძელდება (არ ჩაითვლება!)
# step  → ნაბიჯი (1 = ყველა, 2 = ყოველი მეორე და ა.შ.)
# უარყოფითი ინდექსები: -1 = ბოლო სიმბოლო, -2 = ბოლოდან მეორე და ა.შ.

# მაგალითები:
სიტყვა = "გამარჯობა"
print(სიტყვა[0:4])     # გამა
print(სიტყვა[:4])      # გამა   (თუ start არ წერია → 0-დან)
print(სიტყვა[4:])      # რじობა  (თუ stop არ წერია → ბოლომდე)
print(სიტყვა[-3:])     # ობა   (ბოლო სამი ასო)
print(სიტყვა[::2])     # გმროა  (ყოველი მეორე ასო)
print(სიტყვა[::-1])    # ყველაზე გავრცელებული → სტრიქონის შემოტრიალება

# გამოყენება:
# • სტრიქონის ნაწილის ამოღება (სახელი, გვარი, თარიღის ნაწილი)
# • სიის/ლისტის გაფილტვრა
# • სწრაფი შემოტრიალება (reversing)
# • ბოლო n ელემენტის ან პირველი n ელემენტის მიღება


