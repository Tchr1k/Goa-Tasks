# 2) სეტები მეგობრებით + გაერთიანება + განსხვავება

friends_goa = {"გიორგი", "ანა", "დათო", "მარიამი", "ლუკა"}  # GOA-ში გაცნობილი მეგობრები

friends = {"ნინო", "სოფო", "თეკლა", "ბექა", "ზურა"}  # GOA-მდე გაცნობილი მეგობრები

# გაერთიანება (union) — ყველა უნიკალური ელემენტი ორივე სეტიდან
all_friends = friends_goa.union(friends)  # ან: friends_goa | friends
print("ყველა მეგობარი:", all_friends)

# განსხვავება (difference) — ელემენტები, რომლებიც მხოლოდ ერთ სეტშია
diff_goa = friends_goa.difference(friends)  # მხოლოდ GOA-ში გაცნობილი
print("მხოლოდ GOA-ში გაცნობილი:", diff_goa)

diff_old = friends.difference(friends_goa)  # მხოლოდ GOA-მდე გაცნობილი
print("მხოლოდ GOA-მდე გაცნობილი:", diff_old)


# 3) სეტი მანქანებით + წაშლა + დამატება

cars = {"BMW M5", "Mercedes AMG GT", "Audi RS7", "Toyota Supra", "Porsche 911"}

print("საწყისი სეტი:", cars)

# წაშლა ყველაზე ნაკლებად საყვარელი (მაგ. Toyota Supra)
cars.remove("Toyota Supra")  # თუ არ არსებობს → KeyError, ან discard() რომ არ გამოიტანოს ერორი

# დამატება საუკეთესო (მაგ. Ferrari SF90)
cars.add("Ferrari SF90")

print("შედეგი:", cars)


